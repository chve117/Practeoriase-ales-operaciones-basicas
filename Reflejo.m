% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
vectorX = str2num(get(handles.edit4,'string'));
indice0 = str2double(get(handles.edit7,'string'));

vectorReflejado = fliplr(vectorX);

figure();
subplot(2,1,1);
x = (1 - indice0):length(vectorX) - indice0;
stem(x, vectorX);
title('Vector X[n]');
xlim([-length(vectorX) length(vectorX)]);
subplot(2,1,2);
x = -(length(vectorX) - indice0):-(1 - indice0);
stem(x, vectorReflejado);
title('Vector X[n] Reflejado');
xlim([-length(vectorX) length(vectorX)]);
s = ['[', regexprep(num2str(vectorReflejado), ' +', ' '), ']'];
set(handles.text13, 'String',  s);




