% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
vectorX = str2num(get(handles.edit4,'string'));
factor = str2num(get(handles.edit5,'string'));
indice0 = str2double(get(handles.edit7,'string'));

vectorDesplazado = vectorX;
figure();
subplot(2,1,1);
x = (1 - indice0):length(vectorX) - indice0;
stem(x, vectorX);
title('Vector X[n]');
xlim([(-length(vectorX)-factor-1) length(vectorX)+factor+1]);
subplot(2,1,2);

if (get(handles.radiobuttonDerecha, "value"))    
    x = (1 - indice0 + factor):length(vectorX) - indice0 + factor;
    stem(x, vectorDesplazado);
else
    x = (1 - indice0 - factor):length(vectorX) - indice0 - factor;
    stem(x, vectorDesplazado);
    
end
title('Vector X[n] Desplazado');
xlim([(-length(vectorX)-factor-1) length(vectorX)+factor+1]);
s = ['[', regexprep(num2str(vectorDesplazado), ' +', ' '), ']'];
set(handles.text13, 'String',  s);





