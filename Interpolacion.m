% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
vectorX = str2num(get(handles.edit4,'string'));
factor = str2num(get(handles.edit5,'string'));
indice0 = str2double(get(handles.edit7,'string'));

vectorInterpolado = zeros(1, length(vectorX) * factor);
vectorInterpolado(1:factor:end) = vectorX(1:end);

if(~(get(handles.radiobuttonCero, "value")))
    for i = 1:length(vectorX)
        if i == length(vectorX)
            w = (0 - vectorX(i))/factor;
        else
            w = (vectorX(i+1) - vectorX(i))/factor;
        end
        aux = ((i-1)*factor)+1;
        for j = 1:(factor - 1)
            vectorInterpolado(aux+j) = vectorInterpolado(aux+j-1) + w;
        end
    end
end

figure();
subplot(2,1,1);
x = (1 - indice0):length(vectorX) - indice0;
stem(x, vectorX);
title('Vector X[n]');
xlim([-length(vectorX) (length(vectorX) + 4)]);
subplot(2,1,2);
x = 0:-(1/factor):(1 - indice0);
x = fliplr(x);
y = 1/factor:(1/factor):length(vectorX) - indice0 + (factor-1)/factor;
z = [x,y];
stem(z, vectorInterpolado);
title('Vector X[n] Interpolado');
xlim([-length(vectorX) (length(vectorX) + 4)]);
s = ['[', regexprep(num2str(vectorInterpolado), ' +', ' '), ']'];
set(handles.text13, 'String',  s);




